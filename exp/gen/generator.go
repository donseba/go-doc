// Package gen experiments with generated template helper namespaces.
//
// This package is intentionally outside the main go-doc path. The experiment is
// useful for exploring @import-like template helpers without implying that
// annotations can magically import Go packages at runtime.
package gen

import (
	"bytes"
	"context"
	"errors"
	"fmt"
	"go/format"
	"go/token"
	"go/types"
	"os"
	"path"
	"path/filepath"
	"regexp"
	"sort"
	"strings"
	"unicode"

	"golang.org/x/tools/go/packages"
)

// Options configures one generated helper namespace.
type Options struct {
	// PackagePath is the Go package to wrap, for example "time" or
	// "github.com/example/app/helpers".
	PackagePath string

	// Namespace is the template function name. If empty, the source package name
	// is used. A namespace of "time" enables template calls like {{ time.Now }}.
	Namespace string

	// PackageName is the generated Go package name. If empty, "godocgen" is used.
	PackageName string
}

// Directive is one experimental @gen declaration.
type Directive struct {
	Namespace   string
	PackagePath string
}

var genPattern = regexp.MustCompile(`(?m)^\s*@gen\s+([A-Za-z_][A-Za-z0-9_]*)\s+([A-Za-z_][A-Za-z0-9_./-]*)\s*$`)

// Generate returns Go source for a template helper namespace.
func Generate(ctx context.Context, opts Options) ([]byte, error) {
	if strings.TrimSpace(opts.PackagePath) == "" {
		return nil, errors.New("gen: PackagePath is required")
	}
	return GenerateDirectives(ctx, opts.PackageName, []Directive{{
		Namespace:   opts.Namespace,
		PackagePath: opts.PackagePath,
	}})
}

// GenerateDirectives returns Go source for multiple template helper namespaces.
func GenerateDirectives(ctx context.Context, packageName string, directives []Directive) ([]byte, error) {
	directives, err := normalizeDirectives(directives)
	if err != nil {
		return nil, err
	}
	if len(directives) == 0 {
		return nil, errors.New("gen: no @gen directives")
	}
	if ctx == nil {
		ctx = context.Background()
	}
	if packageName == "" {
		packageName = "godocgen"
	}
	if !validIdentifier(packageName) || !token.IsIdentifier(packageName) {
		return nil, fmt.Errorf("gen: invalid generated package name %q", packageName)
	}

	g := generator{
		packageName: packageName,
		imports:     map[string]string{"html/template": "template"},
	}
	for _, directive := range directives {
		if err := g.addNamespace(ctx, directive); err != nil {
			return nil, err
		}
	}

	var out bytes.Buffer
	fmt.Fprintf(&out, "// Code generated by go-doc exp/gen; DO NOT EDIT.\n")
	fmt.Fprintf(&out, "package %s\n\n", packageName)
	g.writeImports(&out)
	for _, namespace := range g.namespaces {
		fmt.Fprintf(&out, "type %s struct{}\n\n", namespace.typeName)
	}
	fmt.Fprintf(&out, "func FuncMap() template.FuncMap {\n")
	fmt.Fprintf(&out, "\treturn template.FuncMap{\n")
	for _, namespace := range g.namespaces {
		fmt.Fprintf(&out, "\t\t%q: func() %s { return %s{} },\n", namespace.name, namespace.typeName, namespace.typeName)
	}
	fmt.Fprintf(&out, "\t}\n")
	fmt.Fprintf(&out, "}\n\n")
	for _, namespace := range g.namespaces {
		for _, fn := range namespace.funcs {
			if err := g.writeFunction(&out, namespace, fn); err != nil {
				return nil, err
			}
		}
	}

	formatted, err := format.Source(out.Bytes())
	if err != nil {
		return out.Bytes(), err
	}
	return formatted, nil
}

// DirectivesFromFiles reads templates and returns all experimental @gen
// declarations.
func DirectivesFromFiles(files ...string) ([]Directive, error) {
	seen := map[string]Directive{}
	var directives []Directive
	for _, file := range files {
		data, err := os.ReadFile(file)
		if err != nil {
			return nil, fmt.Errorf("gen: read %s: %w", file, err)
		}
		for _, match := range genPattern.FindAllStringSubmatch(contractScanText(string(data)), -1) {
			directive := Directive{
				Namespace:   match[1],
				PackagePath: normalizePackagePath(match[2]),
			}
			previous, exists := seen[directive.Namespace]
			if exists && previous.PackagePath != directive.PackagePath {
				return nil, fmt.Errorf("gen: @gen %s declared as both %s and %s", directive.Namespace, previous.PackagePath, directive.PackagePath)
			}
			if exists {
				continue
			}
			seen[directive.Namespace] = directive
			directives = append(directives, directive)
		}
	}
	return directives, nil
}

// DirectivesFromDir walks root and reads all template files that may contain
// experimental @gen declarations.
func DirectivesFromDir(root string) ([]Directive, error) {
	var files []string
	err := filepath.WalkDir(root, func(file string, entry os.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if entry.IsDir() {
			if shouldSkipDir(entry.Name()) && file != root {
				return filepath.SkipDir
			}
			return nil
		}
		if isTemplateFile(file) {
			files = append(files, file)
		}
		return nil
	})
	if err != nil {
		return nil, fmt.Errorf("gen: scan templates under %s: %w", root, err)
	}
	sort.Strings(files)
	return DirectivesFromFiles(files...)
}

func shouldSkipDir(name string) bool {
	switch name {
	case ".git", ".idea", ".vscode", "vendor", "node_modules", "dist", "build":
		return true
	default:
		return false
	}
}

func isTemplateFile(file string) bool {
	switch strings.ToLower(filepath.Ext(file)) {
	case ".gohtml", ".tmpl", ".html":
		return true
	default:
		return false
	}
}

func normalizeDirectives(directives []Directive) ([]Directive, error) {
	seen := map[string]bool{}
	namespaces := map[string]string{}
	out := make([]Directive, 0, len(directives))
	for _, directive := range directives {
		directive.Namespace = strings.TrimSpace(directive.Namespace)
		directive.PackagePath = normalizePackagePath(directive.PackagePath)
		if directive.PackagePath == "" {
			continue
		}
		if previous := namespaces[directive.Namespace]; previous != "" && previous != directive.PackagePath {
			return nil, fmt.Errorf("gen: @gen %s declared as both %s and %s", directive.Namespace, previous, directive.PackagePath)
		}
		namespaces[directive.Namespace] = directive.PackagePath
		key := directive.Namespace + "\x00" + directive.PackagePath
		if seen[key] {
			continue
		}
		seen[key] = true
		out = append(out, directive)
	}
	return out, nil
}

func (g *generator) addNamespace(ctx context.Context, directive Directive) error {
	if strings.TrimSpace(directive.PackagePath) == "" {
		return errors.New("gen: PackagePath is required")
	}
	fileSet := token.NewFileSet()
	pkgs, err := packages.Load(&packages.Config{
		Context: ctx,
		Mode: packages.NeedName |
			packages.NeedTypes |
			packages.NeedTypesInfo |
			packages.NeedCompiledGoFiles,
		Fset: fileSet,
	}, directive.PackagePath)
	if err != nil {
		return err
	}
	pkg, err := selectPackage(pkgs)
	if err != nil {
		return err
	}

	namespace := directive.Namespace
	if namespace == "" {
		namespace = pkg.Name
	}
	if !validIdentifier(namespace) {
		return fmt.Errorf("gen: invalid namespace %q", namespace)
	}
	sourceAlias := uniqueImportAlias(pkg.Types.Path(), pkg.Name, g.imports)
	g.imports[pkg.Types.Path()] = sourceAlias
	ns := namespaceSpec{
		name:        namespace,
		typeName:    exportedName(namespace) + "Namespace",
		sourceAlias: sourceAlias,
	}
	funcs := exportedFunctions(pkg.Types.Scope())
	for _, fn := range funcs {
		g.collectSignatureImports(fn)
	}
	ns.funcs = funcs
	g.namespaces = append(g.namespaces, ns)
	return nil
}

type generator struct {
	packageName string
	imports     map[string]string
	namespaces  []namespaceSpec
}

type namespaceSpec struct {
	name        string
	typeName    string
	sourceAlias string
	funcs       []*types.Func
}

func selectPackage(pkgs []*packages.Package) (*packages.Package, error) {
	var withTypes []*packages.Package
	for _, pkg := range pkgs {
		if pkg == nil {
			continue
		}
		if len(pkg.Errors) > 0 {
			return nil, pkg.Errors[0]
		}
		if pkg.Types != nil {
			withTypes = append(withTypes, pkg)
		}
	}
	if len(withTypes) == 0 {
		return nil, errors.New("gen: package not found")
	}
	return withTypes[0], nil
}

func exportedFunctions(scope *types.Scope) []*types.Func {
	var funcs []*types.Func
	for _, name := range scope.Names() {
		if !token.IsExported(name) {
			continue
		}
		fn, ok := scope.Lookup(name).(*types.Func)
		if !ok {
			continue
		}
		sig, ok := fn.Type().(*types.Signature)
		if !ok || sig.TypeParams().Len() > 0 {
			continue
		}
		funcs = append(funcs, fn)
	}
	sort.Slice(funcs, func(i, j int) bool {
		return funcs[i].Name() < funcs[j].Name()
	})
	return funcs
}

func (g *generator) collectSignatureImports(fn *types.Func) {
	sig, ok := fn.Type().(*types.Signature)
	if !ok {
		return
	}
	collectTuplePackages(sig.Params(), g.addImport)
	collectTuplePackages(sig.Results(), g.addImport)
}

func collectTuplePackages(tuple *types.Tuple, add func(*types.Package)) {
	if tuple == nil {
		return
	}
	for i := range tuple.Len() {
		collectTypePackages(tuple.At(i).Type(), add)
	}
}

func collectTypePackages(typ types.Type, add func(*types.Package)) {
	switch t := types.Unalias(typ).(type) {
	case *types.Basic:
	case *types.Pointer:
		collectTypePackages(t.Elem(), add)
	case *types.Slice:
		collectTypePackages(t.Elem(), add)
	case *types.Array:
		collectTypePackages(t.Elem(), add)
	case *types.Map:
		collectTypePackages(t.Key(), add)
		collectTypePackages(t.Elem(), add)
	case *types.Chan:
		collectTypePackages(t.Elem(), add)
	case *types.Signature:
		collectTuplePackages(t.Params(), add)
		collectTuplePackages(t.Results(), add)
	case *types.Named:
		if obj := t.Obj(); obj != nil {
			add(obj.Pkg())
		}
		for i := range t.TypeArgs().Len() {
			collectTypePackages(t.TypeArgs().At(i), add)
		}
	case *types.Struct:
		for i := range t.NumFields() {
			collectTypePackages(t.Field(i).Type(), add)
		}
	case *types.Interface:
		for i := range t.NumExplicitMethods() {
			collectTypePackages(t.ExplicitMethod(i).Type(), add)
		}
	}
}

func (g *generator) addImport(pkg *types.Package) {
	if pkg == nil || pkg.Path() == "" {
		return
	}
	if _, ok := g.imports[pkg.Path()]; ok {
		return
	}
	g.imports[pkg.Path()] = uniqueImportAlias(pkg.Path(), pkg.Name(), g.imports)
}

func uniqueImportAlias(pkgPath, suggested string, imports map[string]string) string {
	alias := sanitizeIdentifier(suggested)
	if alias == "" {
		alias = sanitizeIdentifier(path.Base(pkgPath))
	}
	if alias == "" {
		alias = "pkg"
	}
	if !aliasInUse(alias, imports) {
		return alias
	}
	for i := 2; ; i++ {
		candidate := fmt.Sprintf("%s%d", alias, i)
		if !aliasInUse(candidate, imports) {
			return candidate
		}
	}
}

func aliasInUse(alias string, imports map[string]string) bool {
	for _, used := range imports {
		if used == alias {
			return true
		}
	}
	return false
}

func (g *generator) writeImports(out *bytes.Buffer) {
	paths := make([]string, 0, len(g.imports))
	for importPath := range g.imports {
		paths = append(paths, importPath)
	}
	sort.Strings(paths)
	fmt.Fprintln(out, "import (")
	for _, importPath := range paths {
		fmt.Fprintf(out, "\t%s %q\n", g.imports[importPath], importPath)
	}
	fmt.Fprintln(out, ")")
	fmt.Fprintln(out)
}

func (g *generator) writeFunction(out *bytes.Buffer, namespace namespaceSpec, fn *types.Func) error {
	sig, ok := fn.Type().(*types.Signature)
	if !ok {
		return nil
	}

	params, args := g.paramsAndArgs(sig)
	results := g.results(sig)

	fmt.Fprintf(out, "func (%s) %s(%s)%s {\n", namespace.typeName, fn.Name(), strings.Join(params, ", "), results)
	if sig.Results().Len() == 0 {
		fmt.Fprintf(out, "\t%s.%s(%s)\n", namespace.sourceAlias, fn.Name(), strings.Join(args, ", "))
		fmt.Fprintln(out, "}")
		fmt.Fprintln(out)
		return nil
	}
	fmt.Fprintf(out, "\treturn %s.%s(%s)\n", namespace.sourceAlias, fn.Name(), strings.Join(args, ", "))
	fmt.Fprintln(out, "}")
	fmt.Fprintln(out)
	return nil
}

func (g *generator) paramsAndArgs(sig *types.Signature) ([]string, []string) {
	params := make([]string, 0, sig.Params().Len())
	args := make([]string, 0, sig.Params().Len())
	for i := range sig.Params().Len() {
		param := sig.Params().At(i)
		name := fmt.Sprintf("v%d", i)
		typeName := g.typeString(param.Type())
		arg := name
		if sig.Variadic() && i == sig.Params().Len()-1 {
			if slice, ok := types.Unalias(param.Type()).(*types.Slice); ok {
				typeName = "..." + g.typeString(slice.Elem())
				arg += "..."
			}
		}
		params = append(params, name+" "+typeName)
		args = append(args, arg)
	}
	return params, args
}

func (g *generator) results(sig *types.Signature) string {
	results := make([]string, 0, sig.Results().Len())
	for i := range sig.Results().Len() {
		results = append(results, g.typeString(sig.Results().At(i).Type()))
	}
	switch len(results) {
	case 0:
		return ""
	case 1:
		return " " + results[0]
	default:
		return " (" + strings.Join(results, ", ") + ")"
	}
}

func (g *generator) typeString(typ types.Type) string {
	return types.TypeString(types.Unalias(typ), func(pkg *types.Package) string {
		if pkg == nil {
			return ""
		}
		if alias, ok := g.imports[pkg.Path()]; ok {
			return alias
		}
		g.addImport(pkg)
		return g.imports[pkg.Path()]
	})
}

func validIdentifier(name string) bool {
	return token.IsIdentifier(name) && name != "_"
}

func exportedName(name string) string {
	if name == "" {
		return "GoDoc"
	}
	runes := []rune(name)
	runes[0] = unicode.ToUpper(runes[0])
	return string(runes)
}

func sanitizeIdentifier(name string) string {
	var out []rune
	for i, r := range name {
		switch {
		case i == 0 && (r == '_' || unicode.IsLetter(r)):
			out = append(out, r)
		case i > 0 && (r == '_' || unicode.IsLetter(r) || unicode.IsDigit(r)):
			out = append(out, r)
		}
	}
	return string(out)
}

func normalizePackagePath(packagePath string) string {
	packagePath = strings.TrimSpace(packagePath)
	lastSlash := strings.LastIndex(packagePath, "/")
	lastDot := strings.LastIndex(packagePath, ".")
	if lastSlash > lastDot {
		return packagePath
	}
	return packagePath
}

var templateCommentPattern = regexp.MustCompile(`(?s)\{\{/\*(.*?)\*/\}\}`)

func contractScanText(src string) string {
	matches := templateCommentPattern.FindAllStringSubmatch(src, -1)
	if len(matches) == 0 {
		return src
	}
	var out strings.Builder
	for _, match := range matches {
		body := strings.TrimSpace(match[1])
		if body == "" {
			continue
		}
		out.WriteString(body)
		out.WriteByte('\n')
	}
	if out.Len() == 0 {
		return src
	}
	return out.String()
}
